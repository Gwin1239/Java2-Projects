package triptypes;

/**
 * This enum presents the four cabin types on standard cruise ships.
 * @author gdjossou
 *
 */
public enum CabinType 
{
	/**
	 * An interior room on the ship with no window.
	 */
	INTERIOR,
	
	/**
	 * An exterior room on the ship with a fixed window.
	 */
	OCEAN_VIEW,
	
	/**
	 * An exterior room on board that has a small private balcony for guests.
	 */
	BALCONY,
	
	/**
	 * A luxury suite on the ship with a large balcony, sitting room, and separate bedroom.
	 */
	SUITE
	

	
	
	
	
	
	
	
	
}
